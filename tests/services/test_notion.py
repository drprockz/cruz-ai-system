"""
Tests for NotionService — thin async wrapper around Notion REST API.

Scope for R11: create a page under an existing parent page or database.
Used by MARK (doc publish), CATCH (meeting notes), PULSE (briefing archive),
RAW (research log).

Contract:
  await svc.create_page(parent_id, title, content)
    → {"created": True, "url": "https://notion.so/...", "page_id": "..."}
    → raises RuntimeError on missing token or non-2xx
"""

from __future__ import annotations

import os
from unittest.mock import AsyncMock, MagicMock, patch

import pytest


def _mock_notion_response(
    status: int = 200,
    page_id: str = "abc-def-123",
    url: str = "https://notion.so/abc-def-123",
):
    resp = MagicMock()
    resp.status_code = status
    resp.text = "" if status < 300 else "unauthorized"
    resp.json = MagicMock(return_value={
        "id": page_id,
        "url": url,
        "object": "page",
    })
    return resp


def _patch_httpx(response):
    client = AsyncMock()
    client.__aenter__ = AsyncMock(return_value=client)
    client.__aexit__ = AsyncMock(return_value=None)
    client.post = AsyncMock(return_value=response)
    return patch("services.notion.httpx.AsyncClient", return_value=client), client


